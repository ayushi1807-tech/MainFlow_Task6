    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
String id = jTextField1.getText().trim();
    String name = jTextField3.getText().trim();
    String email = jTextField4.getText().trim();
    String phone = jTextField2.getText().trim();
    String address = jTextArea1.getText().trim();

    if (id.equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter Buyer ID.");
        return;
    }

    try {
        Connection con = DBconnection.getConnection();

        
        String selectSql = "SELECT * FROM buyers WHERE id=?";
        PreparedStatement selectPst = con.prepareStatement(selectSql);
        selectPst.setString(1, id);
        ResultSet rs = selectPst.executeQuery();

        if (rs.next()) {
            // Use existing values if fields are empty
            if (name.equals("")) name = rs.getString("name");
            if (email.equals("")) email = rs.getString("email");
            if (phone.equals("")) phone = rs.getString("phone");
            if (address.equals("")) address = rs.getString("address");

            // Now update
            String updateSql = "UPDATE buyers SET name=?, email=?, phone=?, address=? WHERE id=?";
            PreparedStatement updatePst = con.prepareStatement(updateSql);
            updatePst.setString(1, name);
            updatePst.setString(2, email);
            updatePst.setString(3, phone);
            updatePst.setString(4,address);
            updatePst.setString(5, id);

            int rows = updatePst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Buyer updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Update failed!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Buyer not found.");
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

