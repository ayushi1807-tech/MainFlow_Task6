    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String id = jTextField1.getText().trim();

        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter Buyer ID to search.");
            return;
        }

        try {
            Connection con = DBconnection.getConnection();
            String sql = "SELECT * FROM buyers WHERE id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                jTextField3.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("email"));
                jTextField2.setText(rs.getString("phone"));
                jTextArea1.setText(rs.getString("address"));
            } else {
                JOptionPane.showMessageDialog(this, "No Buyer found with ID: " + id);
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

